public class PrintGFGNTimes {
          public static void main(String[] args) {
             int a=5; 
             printGfg(a);
          }  
       static    void printGfg(int N) {
              for (int i = 0; i < N; i++) {
                 System.out.print("GFG ");
              }
          }

          
}
